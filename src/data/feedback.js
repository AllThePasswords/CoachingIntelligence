export const getRecentFeedback = (managerId, days = 30) => {
  const cutoff = new Date();
  cutoff.setDate(cutoff.getDate() - days);
  return feedbackLog.filter(f =>
    f.manager_id === managerId && new Date(f.date) >= cutoff
  );
};
